package de.superlandnetwork.spigot.system.listeners;

import org.bukkit.ChatColor;
import org.bukkit.event.EventHandler;
import org.bukkit.event.Listener;
import org.bukkit.event.block.SignChangeEvent;

import java.util.Objects;

public class SignListener implements Listener {

    @EventHandler
    public void onSignChange(SignChangeEvent e) {
        e.setLine(0, ChatColor.translateAlternateColorCodes('&', Objects.requireNonNull(e.getLine(0))));
        e.setLine(1, ChatColor.translateAlternateColorCodes('&', Objects.requireNonNull(e.getLine(1))));
        e.setLine(2, ChatColor.translateAlternateColorCodes('&', Objects.requireNonNull(e.getLine(2))));
        e.setLine(3, ChatColor.translateAlternateColorCodes('&', Objects.requireNonNull(e.getLine(3))));
    }

}
